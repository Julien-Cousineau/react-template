# Manual Setup

# User node 10
nvm install 10
# Basic environment package
npm i -D dotenv esm faucet

# SASS
npm i -D node-sass

# Webpack
npm i -D webpack webpack-cli webpack-dev-server 
# Webpack extensions
npm i -D html-webpack-plugin html-webpack-root-plugin
# Webpack loaders
npm i -D css-loader sass-loader style-loader url-loader react-hot-loader

# Babel
npm i -D babel-loader @babel/core @babel/preset-env @babel/preset-react @babel/plugin-proposal-class-properties @babel/plugin-proposal-decorators @babel/plugin-transform-runtime babel-plugin-import

# React
npm i -S react react-dom react-router react-router-dom classnames prop-types
# Redux
npm i -S redux react-redux redux-logger redux-thunk

# Eslint
npm i -D eslint eslint-loader babel-eslint eslint-plugin-react eslint-plugin-import eslint-config-airbnb eslint-plugin-jsx-a11y

# SASS & Wix 
npm i -D node-sass stylable @stylable/webpack-plugin @stylable/core
npm i -S wix-style-react

# Storybook
npm i -D @storybook/react
